<?php

namespace Blog\MenuBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Slider
 */
class Slider
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $aHref;

    /**
     * @var string
     */
    private $imgSrc;

    /**
     * @var string
     */
    private $imgAltText;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set aHref
     *
     * @param string $aHref
     * @return Slider
     */
    public function setAHref($aHref)
    {
        $this->aHref = $aHref;
    
        return $this;
    }

    /**
     * Get aHref
     *
     * @return string 
     */
    public function getAHref()
    {
        return $this->aHref;
    }

    /**
     * Set imgSrc
     *
     * @param string $imgSrc
     * @return Slider
     */
    public function setImgSrc($imgSrc)
    {
        $this->imgSrc = $imgSrc;
    
        return $this;
    }

    /**
     * Get imgSrc
     *
     * @return string 
     */
    public function getImgSrc()
    {
        return $this->imgSrc;
    }

    /**
     * Set imgAltText
     *
     * @param string $imgAltText
     * @return Slider
     */
    public function setImgAltText($imgAltText)
    {
        $this->imgAltText = $imgAltText;
    
        return $this;
    }

    /**
     * Get imgAltText
     *
     * @return string 
     */
    public function getImgAltText()
    {
        return $this->imgAltText;
    }
}
